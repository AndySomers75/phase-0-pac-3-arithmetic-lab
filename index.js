function add(a,b){
    return a+b;
}

function subtract(a,b){
    return a-b;
}

function multiply(a,b){
return a*b
}
function divide(x,y){
return x/y
}


function increment(x){
    return x+1
}

function decrement(x){
    return x-1
}

function makeInt(a){
    return parseInt(a,10)
}

function preserveDecimal(x){
    return parseFloat(x)
}